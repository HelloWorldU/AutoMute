// 把 frontend 的 Vite 构建产物（单文件 index.html）嵌进 automute/gui_html.cpp 的
// kIndexHtml 字符串。独立步骤、与 CMake 解耦：改界面 = 改 frontend/ → npm run build →
// node scripts/embed-frontend.mjs → 重编 automute_gui。
//
//   用法:  node scripts/embed-frontend.mjs
//
import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const src = join(root, 'frontend', 'dist', 'index.html')
const dst = join(root, 'automute', 'gui_html.cpp')

if (!existsSync(src)) {
  console.error(`未找到 ${src}\n先在 frontend/ 跑: npm install && npm run build`)
  process.exit(1)
}

const html = readFileSync(src, 'utf8')

// C++ 原始字符串分隔符。极不可能出现在内联产物里；真撞了就报错换分隔符。
const delim = 'AUTOMUTEUI'
if (html.includes(`)${delim}"`)) {
  console.error(`HTML 含分隔符冲突 ")${delim}"，请改 delim`)
  process.exit(1)
}

const header =
  `// AUTO-GENERATED by scripts/embed-frontend.mjs —— 内容来自 frontend/ 的 Vue 构建产物。\n` +
  `// 【不要手改本文件】。改界面请改 frontend/，再跑:\n` +
  `//   cd frontend && npm run build && cd .. && node scripts/embed-frontend.mjs\n` +
  `// 然后重编 automute_gui。\n`

const out = `${header}const char* kIndexHtml = R"${delim}(${html})${delim}";\n`
writeFileSync(dst, out)
console.log(`已嵌入 ${html.length} 字节 → ${dst}`)
